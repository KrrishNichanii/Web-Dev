// Write a program that grades students based on their marks
// 1) If greater than 90 than a A Grade 
// 2) If between 70 and 90 than a B grade
// 3) If between 50 and 70 than a C grade
// 4) Below 50 than a F grade


let grade=40

if(grade>=90) console.log(`Received grade A`);
else if(grade>=70) console.log(`Received grade B`);
else if(grade>=50) console.log(`Received grade C`);
else console.log(`Received grade F`);